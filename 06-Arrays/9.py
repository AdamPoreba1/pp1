array = [2, 3, 7, 5, 4]

print(array)                             #the array
print(len(array))                        #number of elements
print(array[0])                          #first value
print(array[1])                          #second value
print(array[4])                          #last value
print(array[3])                          #last but one value
print(array[0] + array[4])               #sum of the first and last value
print(array[len(array)//2])              #middle value
for n in array:
    print(n,end=" ")                     #all array values separated by a single space (use a loop statement


    


