# determine radius and PI
circle_radius = float(5)
PI = 3.14159
# calculate area 
area = (circle_radius * PI **2)
# calculate circumference
circumference = (2*PI*circle_radius)
# display results
print(f"Radius of the circle:", circle_radius)
print(f"Area of the circle:", area)
print(f"Circumference of the circle:", circumference)